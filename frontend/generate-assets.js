#!/usr/bin/env node

/**
 * Generate placeholder PNG assets for Expo app development
 * This creates minimal valid PNG files (1x1 transparent)
 */

const fs = require('fs');
const path = require('path');

// Minimal 1x1 transparent PNG as base64
const minimalPNG = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, 0x00, 0x00, 0x00, 0x0d,
  0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4, 0x89, 0x00, 0x00, 0x00,
  0x0a, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9c, 0x63, 0x00, 0x01, 0x00, 0x00,
  0x05, 0x00, 0x01, 0x0d, 0x0a, 0x2d, 0xb4, 0x00, 0x00, 0x00, 0x00, 0x49,
  0x45, 0x4e, 0x44, 0xae, 0x42, 0x60, 0x82
]);

const assetsDir = path.join(__dirname, 'assets');
const files = [
  'icon.png',
  'splash.png',
  'adaptive-icon.png',
  'favicon.png'
];

// Ensure assets directory exists
if (!fs.existsSync(assetsDir)) {
  fs.mkdirSync(assetsDir, { recursive: true });
}

// Create placeholder PNG files
files.forEach(filename => {
  const filepath = path.join(assetsDir, filename);
  if (!fs.existsSync(filepath)) {
    fs.writeFileSync(filepath, minimalPNG);
    console.log(`✅ Created: assets/${filename}`);
  } else {
    console.log(`⏭️  Already exists: assets/${filename}`);
  }
});

console.log('\n✅ Assets generated successfully!');
